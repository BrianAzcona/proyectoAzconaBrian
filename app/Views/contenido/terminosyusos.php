      <h4 id="compra">1. Compra</h4>
      <p>
        Al comprar y abonar mediante depósito o transferencia, acepta informar el pago mediante el sitio web antes de la fecha de vencimiento de la reserva para que no sea dada de baja. <br>
        Los datos proporcionados para la facturación deben coincidir con los del titular de la transferencia.
      </p>

      <h4 id="garantia">2. Garantía</h4>
      <p>
        Al utilizar el servicio de Post Venta de Codi Games S.A. usted acepta los términos y condiciones de la garantía. Codi Games S.A. ofrece garantía sobre los productos vendidos, la venta se realiza exclusivamente a través de la página web www.codigames.com. Bajo ningún concepto se realizan ventas por otra vía/medio.
      </p>
      <p>
        Existen marcas y fabricantes que desde el minuto cero de la compra deciden brindar el servicio de postventa de forma directa a los clientes finales. En esos casos Codi Games S.A. derivará a los clientes a los canales de comunicación para que realicen la gestión correspondiente, siempre brindando soporte en el proceso. Para más información, le aconsejamos leer el apartado ¿Cómo tramito la garantía con la marca?
      </p>

      <h5 id="validez-garantia">A. Validez de la garantía</h5>
      <p>
        Toda garantía de producto queda sujeta a la revisión y validación por parte del personal técnico de Codi Games S.A. quien determinará si el producto cumple con las condiciones de garantía de acuerdo con las políticas de cada marca. La comunicación telefónica, web, mail, el pedido de información, coordinación y/o envío del producto hacia las instalaciones de Codi Games S.A. no conforman la aceptación del producto como en garantía.
      </p>
      <p>
        Para productos en que la marca solicita que el consumidor final tramite la garantía directamente, Codi Games S.A. solo está obligada a compartir la información de contacto del fabricante, aunque acompañará al cliente en el trámite sin asumir responsabilidad sobre el resultado.
      </p>

      <h5 id="tiempo-garantia">B. Tiempo de garantía</h5>
      <p>
        El tiempo de garantía del producto se indica en la factura de compra. Si se compran varios ítems, cada uno tendrá su tiempo de garantía indicado por separado. Es requisito conservar caja, empaque, manuales, etiquetas y accesorios para hacer el trámite. Si el fabricante ofrece una garantía mayor, luego del plazo ofrecido por Codi Games S.A., el cliente debe dirigirse directamente a la marca.
      </p>

      <h5 id="cobertura-garantia">C. Cobertura de garantía</h5>
      <p>
        Se cubrirán fallas por uso normal del producto. El equipo técnico verificará que el daño no fue causado por mal uso. Se excluye cualquier producto intervenido o dañado por virus, golpes, rayones, manipulación negligente, o sin etiquetas. En ningún caso se reemplazará un producto sin revisión previa que determine que cumple las condiciones de garantía.
      </p>

      <h5 id="devolucion">D. Devolución</h5>
      <p>
        Dentro de los 30 días corridos desde la recepción, el cliente puede solicitar la devolución. El producto debe estar sin uso y con todos los componentes. Codi Games S.A. verificará el estado y notificará si se otorga o no la Nota de Crédito. Si el producto no califica, se devolverá por correo o podrá retirarse. La Nota de Crédito será por el mismo valor abonado, sin importar si el producto cambió de precio.
      </p>

      <h5 id="salvaguarda">E. Salvaguarda de información</h5>
      <p>
        La protección de archivos o programas es responsabilidad exclusiva del cliente. Codi Games S.A. no se responsabiliza por pérdida de datos o lucro cesante por fallas en los productos.
      </p>

      <h5 id="garantia-terceros">F. Garantía solicitada por terceros</h5>
      <p>
        Para que un tercero tramite la garantía, debe presentar una nota del titular autorizándolo, incluyendo nombre, apellido y DNI de ambos, y una copia del DNI del titular.
      </p>

      <h5 id="reparacion-reemplazo">G. Reparación o reemplazo de productos</h5>
      <p>
        Los productos podrán ser reparados o reemplazados por nuevos o reacondicionados con funcionamiento equivalente, según criterio exclusivo de Codi Games S.A.
      </p>

      <h5 id="envio-service">H. Envío/retiro del Service</h5>
      <p>
        Una vez solucionado el servicio, se debe coordinar el retiro/envío. Codi Games S.A. guardará los productos por un máximo de 90 días, luego podrá disponer de ellos sin derecho a reclamo.
      </p>

      <h5 id="fuera-garantia">I. Fuera de garantía</h5>
      <p>
        Si un producto es declarado fuera de garantía, se informará al cliente para su devolución. Si Codi Games S.A. lo considera viable, se puede intentar una reparación sin que esto implique aceptación en garantía ni garantía sobre el resultado. Las causas de anulación incluyen mal uso, humedad, golpes, enmiendas en la factura o certificado, entre otros. La política de píxeles quemados en monitores depende de cada marca.
      </p>

      <h5 id="recepcion-paquetes">J. Recepción de paquetes en mal estado</h5>
      <p>
        No se deben recibir paquetes con envoltorios abiertos o dañados. En caso de hacerlo, se debe dejar constancia en la planilla del correo e informar a Codi Games S.A. dentro de las 24 horas por la sección AYUDA de la web. Pasado ese plazo, se considera recibido en buenas condiciones. Si al momento de tramitar la garantía se detecta daño físico no informado oportunamente, se rechazará la garantía.
      </p>
